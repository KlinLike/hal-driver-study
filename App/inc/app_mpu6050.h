/**
 * @file app_mpu6050.h
 * @brief MPU6050 演示：HAL I2C 的两种使用方式（课程 041 / 042 配套）
 *
 * 同一份 .c 通过 APP_MODE_SELECT 编译期切换两种实现，便于直观对比：
 *   APP_MODE_MPU6050_POLL  -> 课程 041：HAL_I2C_Mem_Read/Write（阻塞查询）
 *   APP_MODE_MPU6050_IT    -> 课程 042：HAL_I2C_Mem_Read_IT/Write_IT（中断异步）
 *
 * 两种方式的"业务流程"完全相同：
 *   上电 -> 写 PWR_MGMT_1=0 唤醒 -> 读 WHO_AM_I(0x75) -> OLED + 串口输出
 *
 * 中断方式的关键点（041 没有的）：
 *   - _IT 函数立即返回，传输在后台进行；通过回调置位完成标志才知道结束
 *   - 必须使能 I2C1_EV / I2C1_ER 两个 NVIC 中断（CubeMX 已勾选）
 *   - 等待标志要带超时，避免 I2C 卡死时陷入 while(1) 死循环
 *
 * ====== 重要踩坑提示（看完再写代码，能省你 1 小时） ======
 * I2C 上 7 位地址 0x68 被多颗 InvenSense/TDK 芯片共用，市面便宜的
 * "GY-521 / MPU6050" 模块上焊的颗粒经常被换成兼容芯片，从 WHO_AM_I
 * (寄存器 0x75) 读到的字节才是真实型号：
 *   0x68 -> MPU-6050 / MPU-6000   (课程标准)
 *   0x70 -> MPU-6500 / ICM-2060x  (最常见的"假 6050"！)
 *   0x71 -> MPU-9250
 *   0x73 -> MPU-9255
 *   0x74/0x75 -> ICM-20608
 * 这些芯片在 PWR_MGMT_1、WHO_AM_I、加速度/陀螺数据寄存器的位置
 * 与 6050 兼容，但部分寄存器（如自检 0x0D~0x10、温度系数）不一样。
 *
 * 所以：
 *  1) 读到 0x68 才是真 6050；
 *  2) 读到 0x70 等其他值，硬件没坏，是模块卖家偷换芯片，本演示
 *     仍能正常完成"读 ID"这一步——只是读出来的不是 0x68。
 *  3) 课程里"写 0x55 到 0x31 再读回"的自检在 6500/ICM 上不成立
 *     （0x31 在 6500 是 I2C_SLV4_ADDR，不会原样回读），所以本文件
 *     不再包含写读自检，避免误判硬件坏。
 */
#ifndef APP_MPU6050_H
#define APP_MPU6050_H

/** 初始化：唤醒芯片 -> 读一次 WHO_AM_I，结果存于模块内部，OLED 与串口同步输出 */
void app_mpu6050_init(void);
/** 整屏：标题 + WHO_AM_I 读值 + 状态（OK / I2C 失败） */
void app_mpu6050_ui_full(void);

#endif /* APP_MPU6050_H */
